def e_distance(char1_pxl, char2_pxl):
    distance = 0
    rows, cols = len(char1_pxl), len(char1_pxl[0])
    for r in range(rows):
        for c in range(cols):
            distance += (char1_pxl[r][c] - char2_pxl[r][c])**2
    return distance

